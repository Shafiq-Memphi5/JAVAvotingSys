    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)votes.getModel();
        String url = "jdbc:mysql://localhost:3306/voting";
        String mysqluser = "root";
        String mysqlpwd = "123456";
        String query = "select name, count(name) from votes group by name order by count(name) desc";
        try {
            Connection ctn = DriverManager.getConnection(url, mysqluser, mysqlpwd);
            Statement smt = ctn.createStatement();
            ResultSet rs = smt.executeQuery(query);
            while(rs.next()){
               String name = rs.getString("name");
               String Name = rs.getString("count(name)");
               model.addRow(new Object[] {name, Name});
            }
            rs.close();
            smt.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Failed to connect to the database: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

